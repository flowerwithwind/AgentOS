def seed_sqlite():
    import sqlite3
    db_dir = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "database")
    db_path = os.path.join(db_dir, "app.db")

    if not os.path.exists(db_path):
        print("[SQLite] 数据库文件不存在，跳过")
        return

    conn = sqlite3.connect(db_path)
    conn.row_factory = sqlite3.Row

    # 检查 lookout_records 是否已有数据
    existing = conn.execute("SELECT COUNT(*) AS cnt FROM lookout_records").fetchone()["cnt"]
    if existing > 30:
        print(f"[SQLite] lookout_records 已有 {existing} 条数据，跳过瞭望样本插入")
    else:
        added = 0
        for sid, title, summary, keyword, sname in LOOKOUT_SAMPLES:
            try:
                conn.execute(
                    "INSERT INTO lookout_records (source_id, title, summary, keyword, source_name, collected_at) "
                    "VALUES (?, ?, ?, ?, ?, datetime('now', '-' || ? || ' hours'))",
                    (sid, title, summary, keyword, sname, str(added * 2))
                )
                added += 1
            except sqlite3.IntegrityError:
                pass
        conn.commit()
        print(f"[SQLite] 瞭望样本插入 {added} 条")

    # 检查 conversations 是否已有数据
    existing_c = conn.execute("SELECT COUNT(*) AS cnt FROM conversations").fetchone()["cnt"]
    if existing_c > 10:
        print(f"[SQLite] conversations 已有 {existing_c} 条数据，跳过对话样本插入")
    else:
        added_c = 0
        for uid, uname, question, answer in CONVERSATION_SAMPLES:
            try:
                conn.execute(
                    "INSERT INTO conversations (user_id, username, question, answer, created_at) "
                    "VALUES (?, ?, ?, ?, datetime('now', '-' || ? || ' hours'))",
                    (uid, uname, question, answer, str(added_c * 2))
                )
                added_c += 1
            except sqlite3.IntegrityError:
                pass
        conn.commit()
        print(f"[SQLite] 对话样本插入 {added_c} 条")

    conn.close()
    print("[SQLite] 完成")


